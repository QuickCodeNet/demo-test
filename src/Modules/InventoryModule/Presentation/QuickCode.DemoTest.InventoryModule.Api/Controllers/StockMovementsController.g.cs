        [ProducesResponseType(StatusCodes.Status200OK, Type = typeof(List<GetByProductResponseDto>))]
        [ProducesResponseType(StatusCodes.Status404NotFound, Type = typeof(string))]
        [ProducesResponseType(StatusCodes.Status400BadRequest, Type = typeof(string))]
        public async Task<IActionResult> GetByProductAsync(int stockMovementsProductId, int? page, int? size)
        {
            if (page < 1)
            {
                var pageNumberError = $"Page Number must be greater than 1";
                logger.LogWarning($"List Error: '{pageNumberError}''");
                return NotFound(pageNumberError);
            }

            var response = await service.GetByProductAsync(stockMovementsProductId, page, size);
            if (HandleResponseError(response, logger, "StockMovement", $"StockMovementsProductId: '{stockMovementsProductId}'") is {} responseError)
                return responseError;
            return Ok(response.Value);
        }
    }
}